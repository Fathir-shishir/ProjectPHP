           <h3>What does earthquake mean and how it occur!!</h3>
           <p class="txt" style="
    line-height: 40px;">An unexpected movement of the Earth's surface is called an earthquake. When two parts of the earth's surface move suddenly in relation to each other along a fault line, due to tectonic forces, an earthquake occurs. A tremendous amount of energy is released in the form of tremors and vibrations, such earthquakes are called 'Tectonic Earthquakes'. The point of origin of an earthquake is called the 'focus', while the point directly above it, on the surface of the earth is called the 'epicentre'. Owing to the strong vibrations around the point of origin, the degree of destruction is greater around the epicentre. The branch of knowledge that deals with the study of earthquakes is called Seismology, and the experts who study seismic waves are called Seismologists.</p>
